long const TutorialHelpMgr::HelpEntries=sizeof(TutorText)/sizeof(GAME_STRING[MAX_THELP_STRINGS_PER_ENTRY]);



BOOL const TutorialHelpMgr::HelpExists(long const i,long const j)
{
	if (!InBounds(i,j))
		return FALSE;
		
	return (TutorText[i][j]!=STR_NO_HELP);
}

GAME_STRING const TutorialHelpMgr::GetHelp(long const i,long const j)
{
	if (!InBounds(i,j))
	{
#ifdef _DEBUG
		fatal_error("trying to access tutorial help outside of array bounds! %s(%li)",__FILE__,__LINE__);
#endif
		return STR_NULL;
	}
	
	return TutorText[i][j];
}
